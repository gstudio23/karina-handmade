        // –ï—Å–ª–∏ —Å—Å—ã–ª–∫–∞ –≤–µ–¥—ë—Ç –Ω–∞ –¥—Ä—É–≥—É—é —Å—Ç—Ä–∞–Ω–∏—Ü—É ‚Äî –Ω–µ –º–µ—à–∞–µ–º –±—Ä–∞—É–∑–µ—Ä—É
